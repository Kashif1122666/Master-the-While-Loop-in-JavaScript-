// Difference between a for loop and while loop 

// for loop: a for loop is an iteration method that is best used when you know the number of iteration ahead
// of time . it's always followed by the initiialization, expression and increment or decrement statments. 

// while loop: A while loop is an iteration method that is best used when you don't know the number of 
// iteration ahead of time. the contents of the loop are executed as long as the expression evaluates to true.

// Example for loop

// for ( count = 1; count < 10; count++) {
//     console.log(count);
// }

// Note: The increament does not have to be ++ , but you'll see that the most often.

// same example with while loop

// let count = 1;
// while (count < 10) {
//     console.log(count);
//     count++;
// }

